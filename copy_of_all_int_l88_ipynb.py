"""#### **2. Strategies for Handling Missing Values**

Based on the analysis above, we have missing values primarily in `entity`, `keywords`, `sentiment`, and `activity`. Here are common approaches:

*   **Dropping Rows/Columns**:
    *   **Drop rows** (`dropna(axis=0)`): Removes any row that contains at least one missing value. This can lead to significant data loss if many rows have scattered missing values.
    *   **Drop columns** (`dropna(axis=1)`): Removes any column that contains at least one missing value. This is typically only done if a column is almost entirely empty or irrelevant.
    
    *Given our `all_int_data`, dropping rows would remove most of our data since `keywords`, `sentiment`, and `activity` are sparse by design. Dropping these specific columns might lose valuable context for events that *do* have them.*

*   **Imputation (Filling Missing Values)**:
    *   **Fill with a constant**: Replace `NaN` with a specific value like `None`, `Unknown`, `0`, or a default category. This is often suitable for categorical columns where `NaN` itself can represent a meaningful category (e.g., 'No Entity Identified').
    *   **Fill with mean/median/mode**: Replace `NaN` with the mean, median, or mode of the column. This is more common for numerical data, but mode can be used for categorical. *Our `confidence` column has no missing values, but if it did, mean/median could be an option.*
    *   **Forward/Backward Fill**: Propagate the next or previous valid observation forward or backward. Useful for time-series data.

Let's demonstrate filling missing values with a constant for our object (string) columns where missingness often implies 'not applicable' or 'unknown'.
"""

