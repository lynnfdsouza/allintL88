"""#### **2. Global Pattern-of-Life Modeling (Conceptual)**

This illustrates a simplified pattern-of-life analysis by detecting deviations from expected frequencies of events. A real system would build sophisticated baseline models for various entities and locations and use anomaly detection algorithms.
"""

def analyze_pattern_of_life(fused_data, attribute_value, column_to_filter="entity", location_prefix=None, lookback_days=7):
    """Simulates pattern-of-life analysis to detect unusual activity for a given attribute/location."""
    print(f"\n--- Analyzing Pattern-of-Life for '{attribute_value}' in '{column_to_filter}' column ---")

    # Filter data for specific attribute value and column
    relevant_events = fused_data[
        (fused_data[column_to_filter] == attribute_value)
    ].copy()

    if location_prefix:
        relevant_events = relevant_events[
            (relevant_events['location'].str.startswith(location_prefix, na=False))
        ]

    if relevant_events.empty:
        print(f"No '{attribute_value}' events found for pattern-of-life analysis in the specified location or column.")
        return None

    # Group events by day to establish a baseline
    daily_events = relevant_events.groupby(relevant_events['timestamp'].dt.date).size().reset_index(name='event_count')

    # Simulate a baseline (e.g., average over past days, excluding today)
    today = fused_data['timestamp'].max().date()
    baseline_data = daily_events[daily_events['timestamp'] < today]

    if baseline_data.empty or len(baseline_data) < 2:
        print("Not enough historical data to establish a reliable baseline for pattern-of-life.")
        current_day_count = daily_events[daily_events['timestamp'] == today]['event_count'].sum()
        print(f"Current day ({today}) event count: {current_day_count}")
        if current_day_count > 0: print("Considered unusual due to activity with no baseline.")
        else: print("No activity observed.")
        return "None" # Return 'None' for no anomaly due to insufficient data

    # Simple average for baseline (real system would use time-series models)
    baseline_avg_events = baseline_data['event_count'].mean()
    baseline_std_events = baseline_data['event_count'].std()

    print(f"Baseline (avg daily events over {len(baseline_data)} days): {baseline_avg_events:.2f} (std: {baseline_std_events:.2f})")

    # Check current day's activity
    current_day_activity = daily_events[daily_events['timestamp'] == today]
    if not current_day_activity.empty:
        current_events = current_day_activity['event_count'].iloc[0]
        print(f"Current day ({today}) event count: {current_events}")

        # Simple anomaly detection: 2 standard deviations above mean
        if current_events > baseline_avg_events + (2 * baseline_std_events):
            print("**Anomaly Detected**: Current activity significantly above established pattern-of-life!")
            return "Anomaly"
        elif current_events > baseline_avg_events:
            print("Increased activity observed, but within expected variations.")
            return "Increased Activity"
        else:
            print("Activity within normal pattern-of-life.")
            return "Normal"
    else:
        print(f"No activity observed for '{attribute_value}' today ({today}).")
        return "No Activity"

